package VariousTasks;

public class Main11 {

	public static void main(String[] args) {

		for (int i = 1; i <= 10; i++) {
			System.out.println(i + " " + "Lubię Java");
		}

		int i = 10;
		while (i > 0) {
			System.out.println(i + " " + "Lubię Java");
			i--;
		}
	}

}

